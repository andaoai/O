import type { RingData } from './types'

/**
 * 十二时辰：以地支命名，每时辰 2 小时，子时 23:00 起。
 *
 *   子 23:00–00:59  丑 01:00–02:59  寅 03:00–04:59  卯 05:00–06:59
 *   辰 07:00–08:59  巳 09:00–10:59  午 11:00–12:59  未 13:00–14:59
 *   申 15:00–16:59  酉 17:00–18:59  戌 19:00–20:59  亥 21:00–22:59
 *
 * 关键：子时跨午夜(中心约 00:00)，故子时格不能从 0° 起算，而要居中骑跨参考点——
 * 子 占 345°–15°，丑 15°–45°，依次每格 30°。与天文盘地支环布局一致。
 *
 * 与「时柱六十甲子」不同——六十甲子时柱含时干（五鼠遁），5 天才走完 60 格；
 * 而十二时辰只取时支，一天循环一圈 12 格，更直观地指示「当下处于哪个时辰」。
 * 当前时辰序号 = 时柱六十甲子序号 % 12（子=0），由视图层算好后高亮。
 */

/** 十二地支（时辰）按子→亥顺序，每格 30 度 */
const branches = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥']

/** 地支五行配色（与天文盘地支环保持一致的色系） */
const colors = [
  '#0D47A1', // 子 水
  '#795548', // 丑 土
  '#2E7D32', // 寅 木
  '#388E3C', // 卯 木
  '#F57C00', // 辰 土
  '#D32F2F', // 巳 火
  '#C62828', // 午 火
  '#EF6C00', // 未 土
  '#FFEB3B', // 申 金
  '#FFF176', // 酉 金
  '#F9A825', // 戌 土
  '#0288D1'  // 亥 水
]

/** 十二时辰：12 格，每格 30 度，子时居中骑跨 0°（345°–15°） */
export const twelveShichen: RingData = {
  startDegree: -90,
  circleColor: '#888888',
  tickColor: '#666666',
  tickWidth: 0.8,
  fontSize: 14,
  items: branches.map((b, i) => ({
    label: b,
    color: colors[i]!,
    startAngle: (i * 30 - 15 + 360) % 360, // 子: 345°；丑: 15°；……
    endAngle: i * 30 + 15
  }))
}
