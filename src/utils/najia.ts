export const TRIGRAM_KUN = 0   // 坤 ☷
export const TRIGRAM_ZHEN = 1  // 震 ☳
export const TRIGRAM_KAN = 2   // 坎 ☵
export const TRIGRAM_DUI = 3   // 兑 ☱
export const TRIGRAM_GEN = 4   // 艮 ☶
export const TRIGRAM_LI = 5    // 离 ☲
export const TRIGRAM_XUN = 6   // 巽 ☴
export const TRIGRAM_QIAN = 7  // 乾 ☰

/** 八经卦名（索引 = bit 值） */
export const TRIGRAM_NAMES: readonly string[] = [
  '坤', '震', '坎', '兑', '艮', '离', '巽', '乾'
]

// ---------------------------------------------------------------------------
// 纳干表（索引 = 经卦 bit 值）
// ---------------------------------------------------------------------------

/** 内卦（下卦）纳干：为初、二、三爻提供天干 */
export const INNER_STEM: readonly string[] = [
  '乙', // 坤 0
  '庚', // 震 1
  '戊', // 坎 2
  '丁', // 兑 3
  '丙', // 艮 4
  '己', // 离 5
  '辛', // 巽 6
  '甲'  // 乾 7
]

/** 外卦（上卦）纳干：为四、五、上爻提供天干 */
export const OUTER_STEM: readonly string[] = [
  '癸', // 坤 0
  '庚', // 震 1
  '戊', // 坎 2
  '丁', // 兑 3
  '丙', // 艮 4
  '己', // 离 5
  '辛', // 巽 6
  '壬'  // 乾 7
]

// ---------------------------------------------------------------------------
// 纳支表（8 × 6，行=经卦 bit 值，列=爻位 0..5，0=初爻→5=上爻）
// ---------------------------------------------------------------------------

/**
 * 八经卦六爻纳支：
 *   - 阳卦（乾震坎艮）阳顺
 *   - 阴卦（坤巽离兑）阴逆
 * 表本身覆盖完整六爻；在复合卦中：
 *   下三爻取"下卦所在行"的前 3 位；
 *   上三爻取"上卦所在行"的第 i 位（i=3..5），
 *   即八经卦本纯卦的四、五、上爻位置。
 */
export const TRIGRAM_BRANCHES: readonly (readonly string[])[] = [
  ['未', '巳', '卯', '丑', '亥', '酉'], // 坤 0（阴逆）
  ['子', '寅', '辰', '午', '申', '戌'], // 震 1（阳顺）
  ['寅', '辰', '午', '申', '戌', '子'], // 坎 2（阳顺）
  ['巳', '卯', '丑', '亥', '酉', '未'], // 兑 3（阴逆）
  ['辰', '午', '申', '戌', '子', '寅'], // 艮 4（阳顺）
  ['卯', '丑', '亥', '酉', '未', '巳'], // 离 5（阴逆）
  ['丑', '亥', '酉', '未', '巳', '卯'], // 巽 6（阴逆）
  ['子', '寅', '辰', '午', '申', '戌']  // 乾 7（阳顺）
]

// ---------------------------------------------------------------------------
// 纯函数
// ---------------------------------------------------------------------------

/**
 * 给定复合卦 value + 爻位 yaoIndex，返回该爻的干支两字。
 *
 * @param value 六爻卦二进制编码 0-63（与 [[sixtyFourGua]] 一致，bit0=初爻）
 * @param yaoIndex 爻位 0..5，0=初爻，5=上爻
 * @returns 干支两字，如 '甲子' / '壬午' / '乙未'
 *
 * @example
 *   najiaOfLine(63, 0) === '甲子'   // 乾 · 初爻
 *   najiaOfLine(63, 3) === '壬午'   // 乾 · 四爻
 *   najiaOfLine(0, 0)  === '乙未'   // 坤 · 初爻
 */
export function najiaOfLine(value: number, yaoIndex: number): string {
  if (yaoIndex < 0 || yaoIndex > 5) {
    throw new Error(`[najia] yaoIndex 必须在 0..5，实际 ${yaoIndex}`)
  }
  if (value < 0 || value > 63) {
    throw new Error(`[najia] value 必须在 0..63，实际 ${value}`)
  }

  const lower = value & 0b111
  const upper = (value >> 3) & 0b111

  // 下三爻 (0,1,2) 用下卦内干；上三爻 (3,4,5) 用上卦外干
  const isLowerHalf = yaoIndex < 3
  const stem = isLowerHalf ? INNER_STEM[lower]! : OUTER_STEM[upper]!
  const branchRow = isLowerHalf ? lower : upper
  const branch = TRIGRAM_BRANCHES[branchRow]![yaoIndex]!

  return stem + branch
}

/**
 * 给定复合卦 value，返回六爻纳甲字符串数组。
 *
 * @param value 六爻卦二进制编码 0-63
 * @returns 长度 6 的干支字符串数组，index 0 = 初爻，index 5 = 上爻
 *
 * @example
 *   najiaLines(63) === ['甲子','甲寅','甲辰','壬午','壬申','壬戌']  // 乾
 *   najiaLines(0)  === ['乙未','乙巳','乙卯','癸丑','癸亥','癸酉']  // 坤
 */
export function najiaLines(value: number): readonly string[] {
  const out: string[] = new Array(6)
  for (let i = 0; i < 6; i++) {
    out[i] = najiaOfLine(value, i)
  }
  return Object.freeze(out)
}
