/**
 * 浑天纳甲法（纳甲 + 纳支）
 *
 * 【体系】
 *   易经八经卦「纳甲法」—— 天干配卦（内卦纳干 + 外卦纳干），地支配爻
 *   （阳卦顺行 / 阴卦逆行）。任一六爻卦，都能派生出六爻各自唯一的干支坐标。
 *
 *   64 卦 × 6 爻 = 384 爻位纳甲（若配合京房 60 卦剔除四正卦，则 60 × 6 = 360）。
 *
 * 【规则】
 *   - 天干配卦（宏观标签）
 *       乾 内=甲 外=壬     坤 内=乙 外=癸
 *       震 内=庚 外=庚     巽 内=辛 外=辛
 *       坎 内=戊 外=戊     离 内=己 外=己
 *       艮 内=丙 外=丙     兑 内=丁 外=丁
 *   - 地支配爻（微观刻度，自初爻→上爻）
 *       乾（阳顺）子寅辰午申戌     坤（阴逆）未巳卯丑亥酉
 *       震（阳顺）子寅辰午申戌     巽（阴逆）丑亥酉未巳卯
 *       坎（阳顺）寅辰午申戌子     离（阴逆）卯丑亥酉未巳
 *       艮（阳顺）辰午申戌子寅     兑（阴逆）巳卯丑亥酉未
 *
 * 【复合卦纳甲推导】
 *   任一六爻卦 value（编码与 [[sixtyFourGua]] 一致：bit0=初爻，阳=1，阴=0）：
 *     下卦(内) = value & 0b111   —— 决定初、二、三爻
 *     上卦(外) = value >> 3      —— 决定四、五、上爻
 *   对第 i 爻（i=0..5）：
 *     - 下三爻（i∈{0,1,2}）：干=下卦"内卦纳干"，支=下卦纳支表第 i 位
 *     - 上三爻（i∈{3,4,5}）：干=上卦"外卦纳干"，支=上卦纳支表第 i 位
 *
 * 【示例】乾卦 value=63（下卦=乾7 上卦=乾7）：
 *     初=甲子 二=甲寅 三=甲辰 四=壬午 五=壬申 上=壬戌 ✅
 */

